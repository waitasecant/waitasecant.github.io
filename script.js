function getLevels(x) {
  if (x > 0 && x < 5) {
    return 1;
  } else if (x >= 5 && x < 7.5) {
    return 2;
  } else if (x >= 7.5 && x < 10) {
    return 3;
  } else if (x >= 10 && x < 15) {
    return 4;
  } else if (x >= 15 && x < 18) {
    return 5;
  } else if (x >= 18 && x < 21.1) {
    return 6;
  } else if (x >= 21.1 && x < 25) {
    return 7;
  } else if (x >= 25 && x < 30) {
    return 8;
  } else if (x >= 30 && x < 42.2) {
    return 9;
  } else if (x >= 42.2) {
    return 10;
  } else {
    return 0;
  }
}

function isRun(x){
  if (x>0) {
    return 1;
  } else {
    return 0;
  }
}
